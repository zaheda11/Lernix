def generate_professional_report(topic, curriculum_data, report):
    """
    University-grade multi-section PDF.
    `report`  dict from llm_service.generate_full_report_data().
    """
    buf = io.BytesIO()
    doc = _CoverDoc(
        buf, topic, curriculum_data,
        pagesize=letter,
        leftMargin=LM, rightMargin=RM,
        topMargin=TM, bottomMargin=BM,
        title=topic,
        author="LERNIX AI",
        subject=f"Curriculum Report — {topic}",
    )
    s = _S()
    story = []
    # page 1 is the canvas-drawn cover; start content from page 2
    story.append(PageBreak())          # triggers cover draw then moves to page 2
    _toc(story, curriculum_data, s)
    _overview(story, topic, curriculum_data, report, s)
    _semesters(story, curriculum_data, s)
    _resources(story, curriculum_data, report, s)
    _skills(story, report, s)
    _career(story, report, s)
    _capstone(story, report, s)
    _visuals(story, curriculum_data, s)
    doc.build(story, canvasmaker=_Canvas)
    buf.seek(0)
    return buf.getvalue()
