# ── Numbered Canvas ───────────────────────────────────────────────
class _Canvas(canvas.Canvas):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._pages = []

    def showPage(self):
        self._pages.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        n = len(self._pages)
        for state in self._pages:
            self.__dict__.update(state)
            self._decorate(n)
            super().showPage()
        super().save()

    def _decorate(self, total):
        pg = self._pageNumber
        self.saveState()
        if pg == 1:                          # cover — no header/footer
            self.restoreState()
            return
        # header rule
        self.setFillColor(C_PRIMARY)
        self.rect(LM, PH - TM + 10, W, 2, fill=1, stroke=0)
        self.setFont("Helvetica", 7.5)
        self.setFillColor(C_MUTED)
        self.drawString(LM, PH - TM + 14, "LERNIX — Professional Curriculum Report")
        self.drawRightString(LM + W, PH - TM + 14,
                             datetime.now().strftime("%B %d, %Y"))
        # footer rule
        self.setFillColor(C_BORDER)
        self.rect(LM, BM - 14, W, 0.5, fill=1, stroke=0)
        self.setFont("Helvetica", 7.5)
        self.setFillColor(C_MUTED)
        self.drawString(LM, BM - 24,
                        "Generated by LERNIX AI  |  IBM Granite  |  ACM/IEEE Standards")
        self.drawRightString(LM + W, BM - 24, f"Page {pg} of {total}")
        self.restoreState()


