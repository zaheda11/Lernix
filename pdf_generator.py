# ── Helpers ──────────────────────────────────────────────────────
def _hr(story):
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_BORDER, spaceAfter=8, spaceBefore=4))


def _section_banner(story, text, s):
    """Full-width indigo banner for section headings."""
    banner_data = [[Paragraph(f"<font color='white'><b>{text}</b></font>", s["h1"])]]
    t = Table(banner_data, colWidths=[W])
    t.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, -1), C_PRIMARY),
        ("TOPPADDING",    (0, 0), (-1, -1), 10),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 10),
        ("LEFTPADDING",   (0, 0), (-1, -1), 14),
        ("RIGHTPADDING",  (0, 0), (-1, -1), 14),
    ]))
    story.append(Spacer(1, 8))
    story.append(t)
    story.append(Spacer(1, 10))


def _info_table(rows, s):
    """Two-column key-value info table."""
    data = [[Paragraph(k, s["label"]), Paragraph(str(v), s["body"])] for k, v in rows]
    t = Table(data, colWidths=[1.8 * inch, W - 1.8 * inch])
    t.setStyle(TableStyle([
        ("VALIGN",        (0, 0), (-1, -1), "TOP"),
        ("TOPPADDING",    (0, 0), (-1, -1), 5),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 5),
        ("LINEBELOW",     (0, 0), (-1, -1), 0.4, C_BORDER),
        ("LEFTPADDING",   (0, 0), (-1, -1), 6),
    ]))
    return t


def _colored_table(header_row, data_rows, s, col_widths=None):
    """Styled table with indigo header row."""
    header = [Paragraph(f"<font color='white'><b>{h}</b></font>", s["label"]) for h in header_row]
    rows = [[Paragraph(str(c), s["body"]) for c in row] for row in data_rows]
    col_widths = col_widths or [W / len(header_row)] * len(header_row)
    t = Table([header] + rows, colWidths=col_widths)
    t.setStyle(TableStyle([
        ("BACKGROUND",    (0, 0), (-1, 0), C_PRIMARY),
        ("BACKGROUND",    (0, 1), (-1, -1), colors.HexColor("#f8fafc")),
        ("ROWBACKGROUNDS",(0, 1), (-1, -1), [colors.white, colors.HexColor("#f1f5f9")]),
        ("GRID",          (0, 0), (-1, -1), 0.4, C_BORDER),
        ("VALIGN",        (0, 0), (-1, -1), "TOP"),
        ("TOPPADDING",    (0, 0), (-1, -1), 6),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
        ("LEFTPADDING",   (0, 0), (-1, -1), 8),
        ("RIGHTPADDING",  (0, 0), (-1, -1), 8),
    ]))
    return t


# ── Diagrams ─────────────────────────────────────────────────────
def _learning_path_flowchart(semesters):
    """Horizontal flowchart of semesters → courses."""
    box_w, box_h, gap = 90, 28, 18
    total_w = min(len(semesters) * (box_w + gap) + gap, W)
    height = 80
    d = Drawing(total_w, height)
    colors_cycle = [C_PRIMARY, C_SECONDARY, C_ACCENT, C_SUCCESS, C_WARN,
                    colors.HexColor("#db2777"), colors.HexColor("#ea580c")]
    x = gap
    for i, sem in enumerate(semesters):
        col = colors_cycle[i % len(colors_cycle)]
        # Box
        d.add(Rect(x, height - box_h - 10, box_w, box_h,
                   fillColor=col, strokeColor=C_WHITE, strokeWidth=1, rx=4, ry=4))
        # Label
        label = f"Sem {sem['semester_number']}"
        courses_count = f"{len(sem['courses'])} courses"
        d.add(String(x + box_w / 2, height - 22, label,
                     fontName="Helvetica-Bold", fontSize=9,
                     fillColor=C_WHITE, textAnchor="middle"))
        d.add(String(x + box_w / 2, height - 34, courses_count,
                     fontName="Helvetica", fontSize=7.5,
                     fillColor=colors.HexColor("#e0e7ff"), textAnchor="middle"))
        # Arrow to next
        if i < len(semesters) - 1:
            ax = x + box_w + 2
            ay = height - box_h / 2 - 10
            d.add(Line(ax, ay, ax + gap - 4, ay,
                       strokeColor=C_LIGHT, strokeWidth=1.2))
            # Arrowhead
            d.add(Polygon([ax + gap - 4, ay,
                           ax + gap - 10, ay + 4,
                           ax + gap - 10, ay - 4],
                          fillColor=C_LIGHT, strokeColor=C_LIGHT))
        x += box_w + gap
    return d


def _skill_progress_chart(skills_data):
    """Horizontal bar chart for skill levels."""
    bar_h = 14
    gap_y = 8
    label_w = 120
    bar_area = W - label_w - 60
    rows = list(skills_data.items())[:8]
    height = len(rows) * (bar_h + gap_y) + 20
    d = Drawing(W, height)
    y = height - bar_h - 10
    bar_colors = [C_PRIMARY, C_SECONDARY, C_ACCENT, C_SUCCESS,
                  C_WARN, colors.HexColor("#db2777"),
                  colors.HexColor("#ea580c"), colors.HexColor("#0891b2")]
    for i, (skill, level) in enumerate(rows):
        pct = min(max(int(level), 0), 100) / 100
        col = bar_colors[i % len(bar_colors)]
        # Background track
        d.add(Rect(label_w, y, bar_area, bar_h,
                   fillColor=colors.HexColor("#e2e8f0"), strokeColor=None))
        # Fill
        d.add(Rect(label_w, y, bar_area * pct, bar_h,
                   fillColor=col, strokeColor=None, rx=3, ry=3))
        # Label
        d.add(String(label_w - 6, y + 3, skill[:20],
                     fontName="Helvetica", fontSize=8,
                     fillColor=C_MID, textAnchor="end"))
        # Percentage
        d.add(String(label_w + bar_area * pct + 4, y + 3, f"{int(pct*100)}%",
                     fontName="Helvetica-Bold", fontSize=7.5,
                     fillColor=C_MID, textAnchor="start"))
        y -= (bar_h + gap_y)
    return d


def _semester_progress_diagram(semesters):
    """Vertical stacked bar showing credit distribution per semester."""
    bar_w = min(50, (W - 60) / max(len(semesters), 1))
    max_credits = max(
        sum(c.get("credits", 3) for c in s["courses"]) for s in semesters
    ) or 20
    chart_h = 100
    gap = 10
    total_w = len(semesters) * (bar_w + gap) + gap + 40
    d = Drawing(total_w, chart_h + 30)
    x = 40
    for sem in semesters:
        total = sum(c.get("credits", 3) for c in sem["courses"])
        bar_height = int((total / max_credits) * chart_h)
        d.add(Rect(x, 20, bar_w, bar_height,
                   fillColor=C_SECONDARY, strokeColor=C_WHITE,
                   strokeWidth=0.5, rx=3, ry=3))
        d.add(String(x + bar_w / 2, 22 + bar_height, str(total),
                     fontName="Helvetica-Bold", fontSize=8,
                     fillColor=C_DARK, textAnchor="middle"))
        d.add(String(x + bar_w / 2, 8, f"S{sem['semester_number']}",
                     fontName="Helvetica", fontSize=8,
                     fillColor=C_LIGHT, textAnchor="middle"))
        x += bar_w + gap
    # Y-axis label
    d.add(String(6, chart_h / 2 + 10, "Credits",
                 fontName="Helvetica-Oblique", fontSize=7.5,
                 fillColor=C_LIGHT, textAnchor="middle"))
    return d


