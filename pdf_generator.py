# ── Helpers ───────────────────────────────────────────────────────
def _hr(story):
    story.append(HRFlowable(width="100%", thickness=0.5,
                             color=C_BORDER, spaceAfter=8, spaceBefore=4))

def _banner(story, text, s):
    """Indigo full-width section banner."""
    t = Table([[Paragraph(text, s["h1"])]], colWidths=[W])
    t.setStyle(TableStyle([
        ("BACKGROUND",    (0,0),(-1,-1), C_PRIMARY),
        ("TOPPADDING",    (0,0),(-1,-1), 10),
        ("BOTTOMPADDING", (0,0),(-1,-1), 10),
        ("LEFTPADDING",   (0,0),(-1,-1), 14),
        ("RIGHTPADDING",  (0,0),(-1,-1), 14),
    ]))
    story.append(Spacer(1, 8))
    story.append(t)
    story.append(Spacer(1, 10))

def _kv(rows, s):
    """Two-column key→value info table."""
    data = [[Paragraph(k, s["lbl"]), Paragraph(str(v), s["body"])] for k, v in rows]
    t = Table(data, colWidths=[1.7*inch, W - 1.7*inch])
    t.setStyle(TableStyle([
        ("VALIGN",        (0,0),(-1,-1), "TOP"),
        ("TOPPADDING",    (0,0),(-1,-1), 4),
        ("BOTTOMPADDING", (0,0),(-1,-1), 4),
        ("LINEBELOW",     (0,0),(-1,-1), 0.4, C_BORDER),
        ("LEFTPADDING",   (0,0),(-1,-1), 6),
        ("BACKGROUND",    (0,0),(0,-1),  colors.HexColor("#f8fafc")),
    ]))
    return t

def _table(header, rows, s, cw=None):
    """Styled table with indigo header."""
    hrow = [Paragraph(f"<b>{h}</b>", s["cv_meta"]) for h in header]
    drows = [[Paragraph(str(c), s["body"]) for c in r] for r in rows]
    cw = cw or [W / len(header)] * len(header)
    t = Table([hrow] + drows, colWidths=cw, repeatRows=1)
    bg = [colors.white, C_STRIPE]
    t.setStyle(TableStyle([
        ("BACKGROUND",    (0,0),(-1,0),  C_PRIMARY),
        ("TEXTCOLOR",     (0,0),(-1,0),  colors.white),
        ("ROWBACKGROUNDS",(0,1),(-1,-1), bg),
        ("GRID",          (0,0),(-1,-1), 0.4, C_BORDER),
        ("VALIGN",        (0,0),(-1,-1), "TOP"),
        ("TOPPADDING",    (0,0),(-1,-1), 5),
        ("BOTTOMPADDING", (0,0),(-1,-1), 5),
        ("LEFTPADDING",   (0,0),(-1,-1), 7),
        ("RIGHTPADDING",  (0,0),(-1,-1), 7),
    ]))
    return t


# ── Charts ────────────────────────────────────────────────────────
def _flowchart(semesters):
    """Semester learning path flowchart."""
    bw, bh, gap = 85, 32, 16
    n = len(semesters)
    total_w = n * (bw + gap) + gap
    d = Drawing(min(total_w, W), 70)
    palette = [C_PRIMARY, C_CYAN, C_VIOLET, C_GREEN, C_AMBER,
               colors.HexColor("#db2777"), colors.HexColor("#ea580c")]
    x = gap
    for i, sem in enumerate(semesters):
        col = palette[i % len(palette)]
        d.add(Rect(x, 18, bw, bh, fillColor=col,
                   strokeColor=colors.white, strokeWidth=0.8, rx=5, ry=5))
        d.add(String(x + bw/2, 38, f"Semester {sem['semester_number']}",
                     fontName="Helvetica-Bold", fontSize=8.5,
                     fillColor=colors.white, textAnchor="middle"))
        d.add(String(x + bw/2, 26, f"{len(sem['courses'])} courses",
                     fontName="Helvetica", fontSize=7,
                     fillColor=colors.HexColor("#e0e7ff"), textAnchor="middle"))
        if i < n - 1:
            ax = x + bw + 2
            ay = 34
            d.add(Line(ax, ay, ax + gap - 4, ay,
                       strokeColor=C_MUTED, strokeWidth=1))
            d.add(Polygon([ax+gap-4, ay, ax+gap-10, ay+4, ax+gap-10, ay-4],
                          fillColor=C_MUTED, strokeColor=C_MUTED))
        x += bw + gap
    return d

def _bar_chart(semesters):
    """Vertical bar chart — credits per semester."""
    n = len(semesters)
    bw = min(48, (W - 50) / max(n, 1))
    totals = [sum(c.get("credits", 3) for c in s["courses"]) for s in semesters]
    max_t = max(totals) if totals else 20
    ch = 90
    total_w = n * (bw + 10) + 50
    d = Drawing(min(total_w, W), ch + 30)
    x = 42
    for i, sem in enumerate(semesters):
        h = int((totals[i] / max_t) * ch)
        col = [C_PRIMARY, C_CYAN, C_VIOLET][i % 3]
        d.add(Rect(x, 20, bw, h, fillColor=col,
                   strokeColor=colors.white, strokeWidth=0.5, rx=3, ry=3))
        d.add(String(x + bw/2, 22 + h, str(totals[i]),
                     fontName="Helvetica-Bold", fontSize=7.5,
                     fillColor=C_DARK, textAnchor="middle"))
        d.add(String(x + bw/2, 8, f"S{sem['semester_number']}",
                     fontName="Helvetica", fontSize=7.5,
                     fillColor=C_MUTED, textAnchor="middle"))
        x += bw + 10
    d.add(String(12, ch/2 + 10, "Credits",
                 fontName="Helvetica-Oblique", fontSize=7,
                 fillColor=C_MUTED, textAnchor="middle"))
    return d

def _skill_bars(skill_levels):
    """Horizontal bar chart for skill mastery."""
    rows = list(skill_levels.items())[:8]
    bar_h, gap_y, lw = 13, 7, 130
    bar_area = W - lw - 50
    height = len(rows) * (bar_h + gap_y) + 20
    d = Drawing(W, height)
    palette = [C_PRIMARY, C_CYAN, C_VIOLET, C_GREEN, C_AMBER,
               colors.HexColor("#db2777"), colors.HexColor("#ea580c"), C_PRIMARY]
    y = height - bar_h - 8
    for i, (skill, level) in enumerate(rows):
        pct = min(max(int(level), 0), 100) / 100
        col = palette[i % len(palette)]
        # track
        d.add(Rect(lw, y, bar_area, bar_h,
                   fillColor=C_BORDER, strokeColor=None))
        # fill
        if pct > 0:
            d.add(Rect(lw, y, bar_area * pct, bar_h,
                       fillColor=col, strokeColor=None, rx=3, ry=3))
        d.add(String(lw - 6, y + 2, skill[:22],
                     fontName="Helvetica", fontSize=7.5,
                     fillColor=C_MID, textAnchor="end"))
        d.add(String(lw + bar_area * pct + 5, y + 2, f"{int(pct*100)}%",
                     fontName="Helvetica-Bold", fontSize=7,
                     fillColor=C_MID, textAnchor="start"))
        y -= (bar_h + gap_y)
    return d


# ── Cover page ────────────────────────────────────────────────────
def _cover(c_canvas, topic, curriculum_data):
    """Draw directly on canvas for a reliable dark cover."""
    c_canvas.saveState()
    # dark background
    c_canvas.setFillColor(C_DARK)
    c_canvas.rect(0, 0, PW, PH, fill=1, stroke=0)
    # top accent stripe
    c_canvas.setFillColor(C_PRIMARY)
    c_canvas.rect(0, PH - 8, PW, 8, fill=1, stroke=0)
    # bottom accent stripe
    c_canvas.rect(0, 0, PW, 5, fill=1, stroke=0)
    # diagonal decorative strip
    c_canvas.setFillColor(colors.HexColor("#1e293b"))
    c_canvas.rect(0, PH * 0.38, PW, PH * 0.62, fill=1, stroke=0)
    # brand label
    c_canvas.setFont("Helvetica", 10)
    c_canvas.setFillColor(colors.HexColor("#94a3b8"))
    c_canvas.drawString(LM, PH - 40, "LERNIX AI  ·  Professional Curriculum Report")
    # topic title
    c_canvas.setFont("Helvetica-Bold", 32)
    c_canvas.setFillColor(colors.white)
    # wrap long titles
    words = topic.split()
    lines, line = [], []
    for w in words:
        line.append(w)
        if c_canvas.stringWidth(" ".join(line), "Helvetica-Bold", 32) > W:
            if len(line) > 1:
                lines.append(" ".join(line[:-1]))
                line = [w]
            else:
                lines.append(" ".join(line))
                line = []
    if line:
        lines.append(" ".join(line))
    y_title = PH - 110
    for ln in lines:
        c_canvas.drawString(LM, y_title, ln)
        y_title -= 42
    # field of study
    c_canvas.setFont("Helvetica", 14)
    c_canvas.setFillColor(colors.HexColor("#94a3b8"))
    c_canvas.drawString(LM, y_title - 10,
                        f"Field of Study:  {curriculum_data.get('field_of_study', '—')}")
    # separator line
    c_canvas.setStrokeColor(C_PRIMARY)
    c_canvas.setLineWidth(1.5)
    c_canvas.line(LM, y_title - 30, LM + W, y_title - 30)
    # meta block
    meta = [
        ("Duration",       f"{curriculum_data.get('duration_semesters','—')} Semesters"),
        ("Total Courses",  str(sum(len(s["courses"]) for s in curriculum_data.get("semesters",[])))),
        ("Generated By",   "LERNIX AI  (IBM Granite)"),
        ("Date",           datetime.now().strftime("%B %d, %Y")),
        ("Paradigm",       "Outcome-Based Education (OBE)"),
        ("Standard",       "ACM / IEEE Joint Guidelines"),
    ]
    y_meta = y_title - 55
    c_canvas.setFont("Helvetica-Bold", 9)
    c_canvas.setFillColor(colors.HexColor("#64748b"))
    for label, val in meta:
        c_canvas.drawString(LM, y_meta, label)
        c_canvas.setFont("Helvetica", 9)
        c_canvas.setFillColor(colors.HexColor("#e2e8f0"))
        c_canvas.drawString(LM + 110, y_meta, val)
        c_canvas.setFont("Helvetica-Bold", 9)
        c_canvas.setFillColor(colors.HexColor("#64748b"))
        y_meta -= 18
    c_canvas.restoreState()


# ── TOC ───────────────────────────────────────────────────────────
def _toc(story, curriculum_data, s):
    story.append(Paragraph("Table of Contents", s["toc_h"]))
    _hr(story)
    entries = [
        ("1", "Program Overview"),
        ("2", "Semester-wise Curriculum"),
    ]
    for sem in curriculum_data.get("semesters", []):
        entries.append(("", f"Semester {sem['semester_number']} — {len(sem['courses'])} Courses"))
    entries += [
        ("3", "Learning Resources"),
        ("4", "Skills Mapping"),
        ("5", "Career Roadmap"),
        ("6", "Capstone Project Recommendations"),
        ("7", "Visual Analytics"),
    ]
    for num, title in entries:
        if num:
            story.append(Paragraph(f"<b>{num}.</b>  {title}", s["toc_1"]))
        else:
            story.append(Paragraph(f"     {title}", s["toc_2"]))
    story.append(PageBreak())


# ── Section 1: Program Overview ───────────────────────────────────
def _overview(story, topic, curriculum_data, report, s):
    _banner(story, "1.  Program Overview", s)
    tc = sum(len(sem["courses"]) for sem in curriculum_data.get("semesters", []))
    cr = sum(c.get("credits", 3)
             for sem in curriculum_data.get("semesters", [])
             for c in sem["courses"])
