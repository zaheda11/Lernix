# ── Section 5: Career Roadmap ────────────────────────────────────
def _build_career(story, report, s):
    _section_banner(story, "5.  Career Roadmap", s)

    career = report.get("career_roadmap", {})
    levels = [
        ("🟢 Beginner Roles",      career.get("beginner_roles", [])),
        ("🔵 Intermediate Roles",  career.get("intermediate_roles", [])),
        ("🟣 Advanced Roles",      career.get("advanced_roles", [])),
    ]
    for level_title, roles in levels:
        if not roles:
            continue
        story.append(Paragraph(level_title, s["h3"]))
        role_rows = []
        for role in roles:
            if isinstance(role, dict):
                role_rows.append([
                    role.get("title", ""),
                    role.get("description", ""),
                    role.get("salary", "—"),
                ])
            else:
                role_rows.append([str(role), "", "—"])
        if role_rows:
            story.append(_colored_table(
                ["Role", "Description", "Avg. Salary"],
                role_rows, s,
                col_widths=[1.8*inch, 3.4*inch, 1.6*inch]
            ))
        story.append(Spacer(1, 10))

    salary = report.get("salary_insights")
    if salary:
        story.append(Paragraph("Salary Insights", s["h2"]))
        if isinstance(salary, dict):
            story.append(_info_table(list(salary.items()), s))
        else:
            story.append(Paragraph(str(salary), s["body"]))
    story.append(PageBreak())


# ── Section 6: Capstone Recommendations ─────────────────────────
def _build_capstone(story, report, s):
    _section_banner(story, "6.  Capstone Project Recommendations", s)

    projects = report.get("capstone_projects", [])
    for i, proj in enumerate(projects, 1):
        cs = []
        if isinstance(proj, dict):
            cs.append(Paragraph(f"{i}. {proj.get('title','Project')}", s["h3"]))
            cs.append(Paragraph(proj.get("description", ""), s["body"]))
            if proj.get("tech_stack"):
                cs.append(Paragraph(
                    f"<b>Tech Stack:</b> {', '.join(proj['tech_stack'])}", s["body"]
                ))
            if proj.get("deliverables"):
                cs.append(Paragraph("<b>Deliverables:</b>", s["label"]))
                for d in proj["deliverables"]:
                    cs.append(Paragraph(f"• {d}", s["bullet"]))
            if proj.get("difficulty"):
                cs.append(Paragraph(f"<b>Difficulty:</b> {proj['difficulty']}", s["body"]))
        else:
            cs.append(Paragraph(f"{i}. {proj}", s["h3"]))
        cs.append(HRFlowable(width="100%", thickness=0.4, color=C_BORDER,
                              spaceAfter=6, spaceBefore=6))
        story.append(KeepTogether(cs))
    story.append(PageBreak())


