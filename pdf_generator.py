def generate_curriculum_pdf(curriculum_data):
    """Original function — basic report for existing curriculum exports."""
    return generate_professional_report(
        topic=curriculum_data.get("export_topic", curriculum_data.get("title", "Curriculum")),
        curriculum_data=curriculum_data,
        report={}
    )


def generate_professional_report(topic, curriculum_data, report):
    """
    Full university-grade PDF report.
    `report` is the enriched data dict from llm_service.generate_full_report_data().
    """
    buffer = io.BytesIO()
    doc = SimpleDocTemplate(
        buffer,
        pagesize=letter,
        leftMargin=54, rightMargin=54,
        topMargin=72, bottomMargin=72,
        title=topic,
        author="LERNIX AI",
        subject=f"Curriculum Report — {topic}",
    )
    s = _styles()
    story = []

    _build_cover(story, topic, curriculum_data, s)
    _build_toc(story, curriculum_data, s)
    _build_overview(story, topic, curriculum_data, report, s)
    _build_semesters(story, curriculum_data, s)
    _build_resources(story, curriculum_data, report, s)
    _build_skills(story, report, s)
    _build_career(story, report, s)
    _build_capstone(story, report, s)
    _build_visuals(story, curriculum_data, s)

    doc.build(story, canvasmaker=NumberedCanvas)
    buffer.seek(0)
    return buffer.getvalue()
