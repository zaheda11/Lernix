# ── Numbered canvas with header/footer ──────────────────────────
class NumberedCanvas(canvas.Canvas):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._saved_page_states = []

    def showPage(self):
        self._saved_page_states.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        total = len(self._saved_page_states)
        for state in self._saved_page_states:
            self.__dict__.update(state)
            self._draw_decorations(total)
            super().showPage()
        super().save()

    def _draw_decorations(self, total):
        self.saveState()
        pg = self._pageNumber
        # Skip cover page decorations
        if pg == 1:
            self.restoreState()
            return
        # Header bar
        self.setFillColor(C_PRIMARY)
        self.rect(54, 745, W, 2, fill=1, stroke=0)
        self.setFont("Helvetica", 8)
        self.setFillColor(C_LIGHT)
        self.drawString(54, 753, "LERNIX — Professional Curriculum Report")
        self.drawRightString(54 + W, 753, datetime.now().strftime("%B %d, %Y"))
        # Footer
        self.setFillColor(C_BORDER)
        self.rect(54, 44, W, 1, fill=1, stroke=0)
        self.setFont("Helvetica", 8)
        self.setFillColor(C_LIGHT)
        self.drawString(54, 32, "Generated by LERNIX AI  |  Powered by IBM Granite")
        self.drawRightString(54 + W, 32, f"Page {pg} of {total}")
        self.restoreState()


